// Variable declaration using let
const result = 2

declareVariables = (result) => {
  let x;
  console.log(x); // undefined
  let y = 2;
  console.log(y); // 2

  // Variables naming
  let $a = "Jesus";
  console.log($a); // This is valid
  let _b = "Holly";
  console.log(_b);
};
declareVariables(result);
